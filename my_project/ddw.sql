/****主编推荐****/
CREATE TABLE ddw_index_zbtj(
  zid INT PRIMARY KEY AUTO_INCREMENT,
  title VARCHAR(64),
  author VARCHAR(128),
  pic VARCHAR(128),
  new_price DECIMAL(10,2),
  old_price DECIMAL(10,2)
