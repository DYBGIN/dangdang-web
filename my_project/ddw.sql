SET NAMES UTF8;
DROP DATABASE IF EXISTS ddw;
CREATE DATABASE ddw CHARSET=UTF8;
USE ddw;


/**商品评论**/
CREATE TABLE ddw_comment(
  id INT PRIMARY KEY AUTO_INCREMENT,
  bid INT,
  ctime DATETIME,
  content VARCHAR(128),
  username  VARCHAR(28),
  isdel INT
)


/**书本详情**/
CREATE TABLE ddw_book(
  lid  INT PRIMARY KEY AUTO_INCREMENT,
  family_id  INT,
  title VARCHAR(64),
  author VARCHAR(64),
  pic  VARCHAR(128),
  detail  VARCHAR(512),
  new_price DECIMAL(10,2),
  old_price DECIMAL(10,2),
  ele_price DECIMAL(10,2),
  comment  INT,         
  subtitle VARCHAR(128),      #副标题
  publish  VARCHAR(64),
  format VARCHAR(32),          #开本
  paper VARCHAR(32),             #胶版纸
  package VARCHAR(32),         #包装
  suit BOOLEAN,               #是否套装
  isbn VARCHAR(32),           #国际标准书号
  detail_pic  VARCHAR(128)
);

/**书本图片**/
CREATE TABLE ddw_book_pic(
  pid INT PRIMARY KEY AUTO_INCREMENT,
  book_id INT,
  sm VARCHAR(128),            #小图片路径
  md VARCHAR(128),            #中图片路径
  lg VARCHAR(128)             #大图片路径
);

/**用户信息**/
CREATE TABLE ddw_user(
  uid INT PRIMARY KEY AUTO_INCREMENT,
  uname VARCHAR(32),
  upwd VARCHAR(32),
  email VARCHAR(64),
  phone VARCHAR(16),

  avatar VARCHAR(128),        #头像图片路径
  user_name VARCHAR(32),      #用户名，如王小明
  gender INT                  #性别  0-女  1-男
);



/**购物车条目**/
CREATE TABLE ddw_shoppingcart_item(
  iid INT PRIMARY KEY AUTO_INCREMENT,
  user_id INT,      #用户编号
  product_id INT,   #商品编号
  count INT,        #购买数量
  is_checked BOOLEAN #是否已勾选，确定购买
);


/****首页轮播广告商品****/
CREATE TABLE ddw_index_carousel(
  cid INT PRIMARY KEY AUTO_INCREMENT,
  img VARCHAR(128),
  title VARCHAR(64),
  href VARCHAR(128)
);

/****书单分类****/
CREATE TABLE ddw_index_family(
  fid INT PRIMARY KEY AUTO_INCREMENT,
  fname VARCHAR(128)
)

/****尾品汇****/
CREATE TABLE ddw_index_ad(
  aid INT PRIMARY KEY AUTO_INCREMENT,
  pic VARCHAR(64)
);

/****新书上架****/
CREATE TABLE ddw_index_newb(
  nid INT PRIMARY KEY AUTO_INCREMENT,
  title VARCHAR(64),
  author VARCHAR(128),
  pic VARCHAR(128),
  new-price DECIMAL(10,2),
  old-price DECIMAL(10,2),
);

/****新书预售****/
CREATE TABLE ddw_index_readyb(
  rid INT PRIMARY KEY AUTO_INCREMENT,
  title VARCHAR(64),
  pic VARCHAR(128),
  new_price DECIMAL(10,2),
  old_price DECIMAL(10,2),
);

/****新书热卖****/
CREATE TABLE ddw_index_soleb(
  sid INT PRIMARY KEY AUTO_INCREMENT,
  title VARCHAR(64),
  pic VARCHAR(128),
  new_price DECIMAL(10,2),
  old_price DECIMAL(10,2),
