/****图书畅销****/
CREATE TABLE ddw_index_tscx(
  tid INT PRIMARY KEY AUTO_INCREMENT,
  title VARCHAR(64),
  pic VARCHAR(128),
  new-price DECIMAL(10,2),
  old-price DECIMAL(10,2),
